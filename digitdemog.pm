=head1 NAME

App::digitdemog

=head1 SYNOPSIS

This module provides a Unix-like command `digitdemog'. 
(And also "strgroup" that is still on developing, which may be integrated into `digitdemog'.)

=head1 DESCRIPTION

想定している使い方 (digitdemog) : 

  1. 何桁目にどんな文字が現れたのかを解読する
  2. -L により、各行の桁数ごとに、どんな値が現れたか、例を取り出す。
 
